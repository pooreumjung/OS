int
sys_get_file_blocks(void)
{
  char *path;   // 파일 경로 (user → kernel)
  int uarr;     // user 공간의 배열 주소 (raw virtual address)
  int max;      // user가 전달한 배열의 최대 크기
  int karr[64]; // kernel 내부 임시 버퍼 (block 번호 저장)

  // arg 0: 파일 경로 문자열
  if(argstr(0, &path) < 0)
    return -1;

  // arg 1: user 배열 주소 (정수 raw address 그대로 전달됨)
  if(argint(1, &uarr) < 0)
    return -1;

  // arg 2: 배열 최대 수용 개수
  if(argint(2, &max) < 0)
    return -1;

  // 2) path → inode 변환
  struct inode *ip = namei(path);
  if(ip == 0)
    return -1;

  // 3) 해당 inode가 실제로 사용하는 block 번호들을 얻어옴
  int n = get_file_blocks_kernel(ip, karr);
  if(n < 0)
    return -1;

  // user 배열이 작다면 그 크기 안에서만 반환
  if(n > max)
    n = max;

  // 4) kernel → user 공간으로 데이터 복사
  if(copyout(myproc()->pgdir, (uint)uarr,
              (char*)karr, sizeof(int) * n) < 0)
    return -1;

  // 5) 총 블록 개수 반환
  return n;
}
